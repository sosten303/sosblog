			<h2>life history of Steve Paul Jobs</h2>
			<ul>
				<li><b>1955</b> - Born in San Francisco, California located in what is now known as Silicon Valley; and he was raised by adoptive parents in Cupertino</li>
				<li><b>1974</b> - Due to finacial challenges, He dropped out of Reed College, in Portland, Oregon, took a job at Atari Corporation as a video game designer in early 1974, and saved enough money for a pilgrimage to India to experience Buddhism.</li>
				<li><b>1976</b> - Steve Jobs together with Steve Wozniak, and administrative supervisor Ronald Wayne founded Apple Computer (now called Apple Inc.) in the garage of Jobs's Los Altos home on Crist Drive. This was motivated after Wozniak designed and developed the Apple I computer and showed it to Jobs, who suggested that they sell it</li>
				<li><b>1985</b> - He resigned from Apple, and founded NeXT Inc. Here the NeXT workstation was technologically advanced and designed for the education sector, and was known for its technical strengths, among them its object-oriented software development system.</li>
				<li><b>1986</b> - Jobs funded the spinout of The Graphics Group (later renamed Pixar) from Lucasfilm's computer graphics division for the price of $10 million, $5 million of which was given to the company as capital and $5 million of which was paid to Lucasfilm for technology rights</li>
				<li><b>1997</b> - Jobs whent back to Apple, the company he had cofounded and became de facto chief. later, Apple bought NeXT for the expansion of Apple company</li>
				<li><b>2007</b> - Apple entered the cellular phone business with the introduction of the iPhone, a multi-touch display cell phone, which also included the features of an iPod and, with its own mobile browser, revolutionized the mobile browsing scene during the reign of Jobs</li>
				<li><b>2011</b> - He died at his Palo Alto, California, due to complications from a relapse of his previously treated islet-cell pancreatic neuroendocrine tumor, which resulted in respiratory arrest</li>
			</ul>
